package com.matthewchiborak.dndcharacterserver.model;

public class FactRequest {

	private String name;

	public FactRequest() {
		super();
	}

	public FactRequest(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
